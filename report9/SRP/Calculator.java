package report9.SRP;

public class Calculator {
    public int AddOperation(int firstNumber, int secondNumber) {
        return firstNumber + secondNumber;
    }

    public int SubstractOperation(int firstNumber, int secondNumber) {
        return firstNumber - secondNumber;
    }

    public int MultiplyOperation(int firstNumber, int secondNumber) {
        return firstNumber * secondNumber;
    }

    public int DivideOperation(int firstNumber, int secondNumber) {
        return firstNumber / secondNumber;
    }
}